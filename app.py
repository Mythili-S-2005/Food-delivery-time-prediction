# app.py
import streamlit as st
import joblib
import numpy as np

# Load the trained model
model = joblib.load('model.pkl')

st.title("üö¥‚Äç‚ôÇÔ∏è Food Delivery Time Predictor")
st.markdown("Predict delivery time (in minutes) based on input values")

# Input fields
age = st.slider("Delivery Person Age", 18, 60, 30, key="age_slider")
rating = st.slider("Delivery Person Rating", 0.0, 5.0, 4.5, key="rating_slider")
distance = st.number_input("Distance (in km)", min_value=0.0, value=3.0, step=0.1, key="distance_input")
order_type = st.selectbox("Type of Order", ["Buffet", "Drinks", "Meal", "Snack"], key="order_type_select")
vehicle_type = st.selectbox("Type of Vehicle", ["Bicycle", "Motorcycle", "Scooter", "Walking"], key="vehicle_type_select")

# Encode categorical values
order_mapping = {"Buffet": 0, "Drinks": 1, "Meal": 2, "Snack": 3}
vehicle_mapping = {"Bicycle": 0, "Motorcycle": 1, "Scooter": 2, "Walking": 3}

encoded_order = order_mapping[order_type]
encoded_vehicle = vehicle_mapping[vehicle_type]

# Predict
if st.button("Predict Time", key="predict_button"):
    input_data = np.array([[age, rating, distance, encoded_order, encoded_vehicle]])
    predicted_time = model.predict(input_data)[0]
    st.success(f"Estimated Delivery Time: **{round(predicted_time, 2)} minutes**")
